fn find_duplicate_permutations() {
    use std::collections::HashMap;
    let mut uniques = Vec::new();
    let mut checked: HashMap<[u8; 12], Vec<u16>> = HashMap::new();
    'codes: for i in 0..ELEVEN_DOUBLE_FACTORAL {
        let permutation_original = int_to_array(i);
        let mut permutation = permutation_original;

        for _ in 0..6 {
            if let Some(entries) = checked.get_mut(&permutation) {
                entries.push(i);
                continue 'codes;
            }
            permutation = rotate(permutation, 1);
        }
        checked.insert(permutation_original, vec![i]);
        uniques.push(i);
    }
    dbg!(checked.len());
    dbg!(checked.len() as f32 / ELEVEN_DOUBLE_FACTORAL as f32);
    let mut counts = [0; 6];
    for (_, entries) in checked {
        counts[entries.len() - 1] += 1;
    }
    dbg!(counts);
    std::fs::write("codes.json", serde_json::to_string(&uniques).unwrap()).unwrap();
}
