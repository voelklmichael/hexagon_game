const DIVISORS: [u16; 6] = [11, 9, 7, 5, 3, 1];
const ELEVEN_DOUBLE_FACTORAL: u16 = {
    /*let mut s = 1;
    for d in DIVISORS{s*=d;}
    s*/
    11 * 9 * 7 * 5 * 3 * 1
};
pub fn int_to_array(input: u16) -> [u8; 12] {
    assert!(input < ELEVEN_DOUBLE_FACTORAL);
    let mut permutation = [0; 12];
    let mut used = [false; 12];
    let mut input = input;
    for (_, divisor) in DIVISORS.into_iter().enumerate() {
        //dbg!(input, i, divisor, &permutation);
        let start = used.iter().position(|&x| x == false).unwrap();
        let end = (input % divisor) as usize;
        //dbg!(i, divisor, start, end);
        let end = used
            .iter()
            .enumerate()
            .filter(|(_, &c)| !c)
            .skip(1)
            .skip(end)
            .next()
            .unwrap()
            .0;
        permutation[start] = end as u8;
        permutation[end] = start as u8;
        used[start] = true;
        used[end] = true;
        //dbg!(permutation, used);
        input = input / divisor;
    }
    permutation
}

pub fn rotate(permutation: [u8; 12], rotation: u8) -> [u8; 12] {
    let mut copy = permutation;
    for i in 0..12 {
        let ii = (i + rotation * 2) % 12;
        let jj = permutation[ii as usize];
        let j = (jj + (12 - rotation * 2)) % 12;
        copy[i as usize] = j
    }
    copy
}

